function get_temp()
	local f = io.open('/proc/acpi/thermal_zone/TZ1/temperature')
	local tzone_contents = f:read("*all")
	f:close()

	local temp, fmt_temp
	_, _, temp = tzone_contents:find('(%d+)')


	fmt_temp = temp.."C "
	if temp > "60" then
		fmt_temp = fg("red", fmt_temp)
	else if temp > "50" then
			fmt_temp = fg("orange", fmt_temp)
		else
			fmt_temp = fg("blue", fmt_temp)
		end
	end

	return { temp, fmt_temp }
end
