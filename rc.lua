function get_charge()
	local acpi = awful.util.pread("acpi -b")
	local charge
	_, _, charge = acpi:find("(%d+)%%")
	local state, prefix, colour, suffix
	charging = acpi:find("Charging")
	discharging = acpi:find("Discharging")
	if charging then
		prefix = "+"
		suffix = "until charged"
	else if discharging then
			prefix = "-"
			suffix = "remaining"
		else
			prefix = ""
			suffix = ""
		end
	end
	if 0+charge > 74 then
		colour = "green"
	else if 0+charge > 50 then
			colour = "blue"
		else
			colour = "red"
		end
	end

	local timeleft
	_, _, timeleft = acpi:find("(%d+:%d+:%d+)")
	if timeleft == nil then
		if acpi:find("Full") then
			timeleft = "Battery"
			suffix = "Full"
		else
			timeleft = "Unknown"
			suffix = "State"
		end
	end

	local status =  fg(colour, " " .. prefix .. charge .. "% ")
	local remaining = fg(colour, timeleft .. " " .. suffix)

	battbarwidget:set_color(colour)
	return { charge, status, remaining }
end
